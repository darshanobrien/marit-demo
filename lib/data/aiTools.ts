  "Use as decision support only; accountable teams must review outputs, uncertainty, and limitations before acting.";

type ApprovedAIToolSeed = Omit<
  AITool,
  | "dataSensitivitySuitability"
  | "supportedLanguages"
  | "accessibilityConsiderations"
  | "responsibleAiNotes"
  | "lastReviewedAt"
  | "riskProfile"
>;

function approvedTool(tool: ApprovedAIToolSeed): AITool {
  return {
    dataSensitivitySuitability: ["public", "confidential"],
    supportedLanguages: {
      english: true,
      french: false,
    },
    accessibilityConsiderations: defaultAccessibilityConsiderations,
    responsibleAiNotes: defaultResponsibleAiNotes,
    lastReviewedAt,
    riskProfile: {
      supportedDataSensitivity: ["low", "medium"],
      riskNotes: tool.limitations,
      requiresHumanReview: true,
    },
    ...tool,
  };
}

